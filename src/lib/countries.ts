/** Format stored value: dial code + digits only (E.164-style). */
export function formatPhoneWithCountry(dial: string, localDigits: string): string {
  const digits = localDigits.replace(/\D/g, "");
  if (!digits) return "";
  const code = dial.replace(/\D/g, "");
  return `${dial.startsWith("+") ? "" : "+"}${code}${digits}`;
}
